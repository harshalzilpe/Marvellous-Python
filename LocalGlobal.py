no = 11

def fun():
    print("Inside fun")
    x = 21
    print("Value of x is",x)
    print("Value of no is",no)
    
def sun():
    print("Inside sun")
    y = 51
    print("Value of x is",y)
    print("Value of no is",no)
    
def main():
    fun()
    sun()
    
if __name__ == "__main__":
    main()